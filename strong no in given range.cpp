#include<stdio.h>
main()
{
	int n,r,i,temp,min,max,sum,fact;
	scanf("%d%d",&min,&max);
	for(n=min;n<=max;n++)
{
	temp=n;
	sum=0;
	while(temp>0)
{
	fact=1;
	r=temp%10;
	for(i=1;i<=r;i++)
{
	fact=fact*i;
}
	sum=sum+fact;
			temp=temp/10;
}
		if(n==sum)
        printf("%d\n",sum);
}
}
